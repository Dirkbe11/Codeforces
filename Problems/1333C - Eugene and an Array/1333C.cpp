struct custom_hash {
    static uint64_t splitmix64(uint64_t x) {
        // http://xorshift.di.unimi.it/splitmix64.c
        x += 0x9e3779b97f4a7c15;
        x = (x ^ (x >> 30)) * 0xbf58476d1ce4e5b9;
        x = (x ^ (x >> 27)) * 0x94d049bb133111eb;
        return x ^ (x >> 31);
    }

    size_t operator()(uint64_t x) const {
        static const uint64_t FIXED_RANDOM = chrono::steady_clock::now().time_since_epoch().count();
        return splitmix64(x + FIXED_RANDOM);
    }
};

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);

    int n;
    cin >> n;

    vector<ll> nums{0};
    for(int i = 0; i < n; i++)
    {
    	ll temp;
    	cin >> temp;
    	nums.push_back(nums.back()+temp);
    }

    ll result = 0;
    int begin = 0, end = 0;;
	std::unordered_set<ll, custom_hash> mapping = {0};

    while(begin < n)
    {
    	while(end < n && !mapping.count(nums[end+1]))
    	{
    		end++;
    		mapping.insert(nums[end]);
    	}

    	result += end-begin;
    	mapping.erase(nums[begin]);
    	begin++;
    }

    std::cout << result << std::endl;

	return 0;
}